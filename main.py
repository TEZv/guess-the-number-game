# -*- coding: utf-8 -*-
import random

def guess_the_number():
    """
    –ö–æ–Ω—Å–æ–ª—å–Ω–∞ –≥—Ä–∞ "–í–≥–∞–¥–∞–π —á–∏—Å–ª–æ", –¥–µ –∫–æ–º–ø'—é—Ç–µ—Ä –∑–∞–≥–∞–¥—É—î –≤–∏–ø–∞–¥–∫–æ–≤–µ —á–∏—Å–ª–æ.
    –í–∫–ª—é—á–∞—î –¥–∏–Ω–∞–º—ñ—á–Ω–∏–π –¥—ñ–∞–ø–∞–∑–æ–Ω —á–∏—Å–µ–ª, –∑–º—ñ–Ω–Ω—É –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø—Ä–æ–± —Ç–∞ "–∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç —É–¥–∞—á—ñ".
    """
    print("–ü—Ä–∏–≤—ñ—Ç–∏–∫–∏ —É –≥—Ä—ñ '–í–≥–∞–¥–∞–π —á–∏—Å–ª–æ'!")
    
    # --- –î–æ–¥–∞—Ç–∫–æ–≤–∞ —Ü—ñ–∫–∞–≤–∏–Ω–∫–∞: –î–∏–Ω–∞–º—ñ—á–Ω–∏–π –¥—ñ–∞–ø–∞–∑–æ–Ω —á–∏—Å–µ–ª —Ç–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø—Ä–æ–± ---
    min_range = random.randint(1, 10)  # –ú—ñ–Ω—ñ–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥—ñ–∞–ø–∞–∑–æ–Ω—É
    max_range = random.randint(50, 200) # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥—ñ–∞–ø–∞–∑–æ–Ω—É
    
    # –ó–∞–±–µ–∑–ø–µ—á—É—î–º–æ, —â–æ–± –º–∞–∫—Å–∏–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –±—É–ª–æ –±—ñ–ª—å—à–∏–º –∑–∞ –º—ñ–Ω—ñ–º–∞–ª—å–Ω–µ
    if min_range >= max_range:
        max_range = min_range + random.randint(50, 100)

    # –î–∏–Ω–∞–º—ñ—á–Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å–ø—Ä–æ–± –∑–∞–ª–µ–∂–Ω–æ –≤—ñ–¥ —Ä–æ–∑–º—ñ—Ä—É –¥—ñ–∞–ø–∞–∑–æ–Ω—É
    # –ù–∞–ø—Ä–∏–∫–ª–∞–¥, (max_range - min_range) / 10 + 3, –æ–∫—Ä—É–≥–ª–µ–Ω–µ –¥–æ —Ü—ñ–ª–æ–≥–æ
    max_attempts = max(5, round((max_range - min_range) / random.uniform(8, 15))) # –ó–º—ñ–Ω—é—î–º–æ –¥—ñ–ª—å–Ω–∏–∫ –≤–∏–ø–∞–¥–∫–æ–≤–æ
    
    print(f"–Ø –∑–∞–≥–∞–¥–∞—é —Ü—ñ–ª–µ —á–∏—Å–ª–æ —É –¥—ñ–∞–ø–∞–∑–æ–Ω—ñ –≤—ñ–¥ {min_range} –¥–æ {max_range}.")
    print(f"–£ —Ç–µ–±–µ –±—É–¥–µ {max_attempts} —Å–ø—Ä–æ–±, —â–æ–± –π–æ–≥–æ –≤–≥–∞–¥–∞—Ç–∏.")

    secret_number = random.randint(min_range, max_range)
    attempts_taken = 0

    # --- –î–æ–¥–∞—Ç–∫–æ–≤–∞ —Ü—ñ–∫–∞–≤–∏–Ω–∫–∞: –í–∏–ø–∞–¥–∫–æ–≤–∏–π "–∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç —É–¥–∞—á—ñ" –¥–ª—è –ø—ñ–¥–∫–∞–∑–æ–∫ ---
    # –¶–µ –¥–æ–¥–∞—î –Ω–µ–≤–µ–ª–∏–∫—É –≤–∏–ø–∞–¥–∫–æ–≤—ñ—Å—Ç—å –¥–æ —Ñ–æ—Ä–º—É–ª—é–≤–∞–Ω–Ω—è –ø—ñ–¥–∫–∞–∑–æ–∫
    luck_factor = random.choice(["—Ç—Ä–æ—Ö–∏", "—Ç—Ä—ñ—à–∫–∏", "–∑–æ–≤—Å—ñ–º –Ω–µ–±–∞–≥–∞—Ç–æ", "–¥–æ–≤–æ–ª—ñ", "–¥—É–∂–µ"])
    
    while attempts_taken < max_attempts:
        try:
            guess_input = input(f"–°–ø—Ä–æ–±–∞ {attempts_taken + 1}/{max_attempts}. –í–≤–µ–¥–∏ —Å–≤–æ—î —á–∏—Å–ª–æ: ")
            
            # –û–±—Ä–æ–±–∫–∞ –≤–≤–µ–¥–µ–Ω–Ω—è –¥–ª—è –≤–∏—Ö–æ–¥—É –∑ –≥—Ä–∏
            if guess_input.lower() == '–≤–∏—Ö—ñ–¥':
                print("–®–∫–æ–¥–∞, —â–æ —Ç–∏ —Ç–∞–∫ —à–≤–∏–¥–∫–æ –∑–¥–∞—î—à—Å—è. –î–æ –∑—É—Å—Ç—Ä—ñ—á—ñ!")
                return
            
            guess = int(guess_input)

            if not (min_range <= guess <= max_range):
                print(f"–ë—É–¥—å –ª–∞—Å–∫–∞, –≤–≤–µ–¥–∏ —á–∏—Å–ª–æ —É –¥—ñ–∞–ø–∞–∑–æ–Ω—ñ –≤—ñ–¥ {min_range} –¥–æ {max_range}.")
                continue # –ü—Ä–æ–¥–æ–≤–∂—É—î–º–æ –¥–æ –Ω–∞—Å—Ç—É–ø–Ω–æ—ó —Å–ø—Ä–æ–±–∏ –±–µ–∑ –∑–±—ñ–ª—å—à–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
            
            attempts_taken += 1 # –ó–±—ñ–ª—å—à—É—î–º–æ –ª—ñ—á–∏–ª—å–Ω–∏–∫ —Ç—ñ–ª—å–∫–∏ —è–∫—â–æ –≤–≤–µ–¥–µ–Ω–æ –∫–æ—Ä–µ–∫—Ç–Ω–µ —á–∏—Å–ª–æ

            difference = abs(secret_number - guess)
            
            if guess < secret_number:
                # --- –î–æ–¥–∞—Ç–∫–æ–≤–∞ —Ü—ñ–∫–∞–≤–∏–Ω–∫–∞: "–†–æ–∑—É–º–Ω—ñ—à—ñ" –ø—ñ–¥–∫–∞–∑–∫–∏ ---
                if difference <= 5:
                    print(f"–ó–∞–Ω–∞–¥—Ç–æ –º–∞–ª–µ–Ω—å–∫–µ! –ê–ª–µ {luck_factor} –±–ª–∏–∑—å–∫–æ!")
                elif difference <= 20:
                    print(f"–ó–∞–Ω–∞–¥—Ç–æ –º–∞–ª–µ–Ω—å–∫–µ. –ê–ª–µ —Ç–∏ {luck_factor} –Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º—É —à–ª—è—Ö—É!")
                else:
                    print("–ó–∞–Ω–∞–¥—Ç–æ –º–∞–ª–µ–Ω—å–∫–µ!")
            elif guess > secret_number:
                if difference <= 5:
                    print(f"–ó–∞–Ω–∞–¥—Ç–æ –≤–µ–ª–∏–∫–µ! –ê–ª–µ {luck_factor} –±–ª–∏–∑—å–∫–æ!")
                elif difference <= 20:
                    print(f"–ó–∞–Ω–∞–¥—Ç–æ –≤–µ–ª–∏–∫–µ. –ê–ª–µ —Ç–∏ {luck_factor} –Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º—É —à–ª—è—Ö—É!")
                else:
                    print("–ó–∞–Ω–∞–¥—Ç–æ –≤–µ–ª–∏–∫–µ!")
            else:
                print(f"\n –í—ñ—Ç–∞–Ω–Ω—è—á–∫–∞! –í–≥–∞–¥–∞–Ω–æ —á–∏—Å–ª–æ {secret_number} –∑–∞ {attempts_taken} —Å–ø—Ä–æ–±! –ú–∞–π—Å—Ç–µ—Ä/–º–∞–π—Å—Ç—Ä–∏–Ω—è –≤–≥–∞–¥—É–≤–∞–Ω–Ω—è - —Ü–µ —Ç–æ—á–Ω–æ –ø—Ä–æ —Ç–µ–±–µ!")
                return # –ó–∞–≤–µ—Ä—à—É—î–º–æ –≥—Ä—É –ø—ñ—Å–ª—è –ø–µ—Ä–µ–º–æ–≥–∏
        except ValueError:
            print("–ù–µ–≤—ñ—Ä–Ω–∏–π –≤–≤—ñ–¥. –ë—É–¥—å –ª–∞—Å–∫–∞, –≤–≤–µ–¥–∏ —Ü—ñ–ª–µ —á–∏—Å–ª–æ –∞–±–æ '–≤–∏—Ö—ñ–¥'.")
            # –ù–µ –∑–±—ñ–ª—å—à—É—î–º–æ attempts_taken, –æ—Å–∫—ñ–ª—å–∫–∏ —Ü–µ –Ω–µ –±—É–ª–∞ –≤–∞–ª—ñ–¥–Ω–∞ —Å–ø—Ä–æ–±–∞

    # –Ø–∫—â–æ –≥—Ä–∞–≤–µ—Ü—å –≤–∏—á–µ—Ä–ø–∞–≤ —É—Å—ñ —Å–ø—Ä–æ–±–∏
    print(f"\nüòî –ù–∞ –∂–∞–ª—å, —É —Ç–µ–±–µ –∑–∞–∫—ñ–Ω—á–∏–ª–∏—Å—å —Å–ø—Ä–æ–±–∏. –ó–∞–≥–∞–¥–∞–Ω–µ —á–∏—Å–ª–æ –±—É–ª–æ: {secret_number}.")
    print("–°–ø—Ä–æ–±—É–π —â–µ —Ä–∞–∑! –ú–æ–∂–ª–∏–≤–æ, —Ü—å–æ–≥–æ —Ä–∞–∑—É —Ç–æ–±—ñ –ø–æ—â–∞—Å—Ç–∏—Ç—å –±—ñ–ª—å—à–µ. üòâ")

# –ó–∞–ø—É—Å–∫ –≥—Ä–∏
if __name__ == "__main__":
    guess_the_number()